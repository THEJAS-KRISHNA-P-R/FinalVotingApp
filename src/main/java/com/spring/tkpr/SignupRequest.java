package com.spring.tkpr;

import jakarta.validation.constraints.*;

public class SignupRequest {

    @NotBlank(message = "Username is required")
    @Size(min = 3, max = 20, message = "Username must be 3-20 characters")
    private String username;

    @NotBlank(message = "Email is required")
    @Email(message = "Please enter a valid email")
    private String email;

    @NotBlank(message = "Password is required")
    @Size(min = 5, max = 10, message = "Password must be 5-10 characters")
    @Pattern(regexp = ".*[A-Z].*", message = "Password must contain at least 1 uppercase letter")
    @Pattern(regexp = ".*\\d.*", message = "Password must contain at least 1 number")
    private String password;

    // Getters and Setters
    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}